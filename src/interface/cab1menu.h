enum COMMAND_STARTING_BIT_TYPE
{
  // The following sequence of A's refer to the Address field of the object receiving the command.
  SWITCH_COMMAND = 0b01000000, // 0 1 A A A A A A
  ROUTE_COMMAND = 0b11000000, // 1 1 0 1 A A A A
  ENGINE_COMMAND = 0b00000000, // 0 0 A A A A A A
  TRAIN_COMMAND = 0b11001000, // Consist Commands.  // 1 1 0 0 1 A A A
  ACCESSORY_COMMAND = 0b10000000, // 1 0 A A A A A A
  GROUP_COMMAND = 0b11000000, // Multiple Accessories activated together. 1 1 0 0 0 A A A
};

enum ACTION_COMMAND_FIELD
{
  // All of these command fields must be shifted to the left 5 bits. 
  ACTION = 0b00000000,
  EXTENDED = 0b00000001,
  RELATIVE_SPEED = 0b00000010,
  ABSOLUTE_SPEED = 0b00000011,
};

